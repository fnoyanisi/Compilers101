#include <stdint.h>
#include <stdio.h>

 typedef enum {IDENT, KEYWORD, NUMBER, STRING, PUNC, ENDFILE} lex_types;

 typedef struct lexeme_t {
     lex_types type;
     uint32_t value;
 } lexeme;

 extern lexeme lex_this;    /* the current lexeme */
 extern lexeme lex_next;    /* the next lexeme */

 void lex_open(char *f);
 void lex_advance();
 void lex_put(lexeme *l, FILE *f);